func threeSumClosestA(nums []int, target int) int {
	diff, n := make(map[int]bool), len(nums)
	for i := 0; i < n; i++ {
		if i > 0 && nums[i] == nums[i-1] {
			continue
		}
		for j := i + 1; j < n; j++ {
			for k := j + 1; k < n; k++ {
				curSum := nums[i] + nums[j] + nums[k]
				if !diff[curSum] {
					diff[curSum] = true
				}
			}
		}
	}
	min, ans := math.MaxInt, 0
	for k := range diff {
		if int(math.Abs(float64(k-target))) < min {
			ans = k
			min = int(math.Abs(float64(k - target)))
		}

	}
	return ans
}

func threeSumClosestB(nums []int, target int) int {
	sort.Ints(nums)
	n, best := len(nums), math.MaxInt

	// æ ¹æ®å·®å€¼çš„ç»å¯¹å€¼æ¥æ›´æ–°ç­”æ¡ˆ
	update := func(cur int) {
		if abs(cur-target) < abs(best-target) {
			best = cur
		}
	}

	for i := 0; i < n; i++ {
		if i > 0 && nums[i] == nums[i-1] {
			continue
		}
		l, r := i+1, n-1
		for l < r {
			sum := nums[i] + nums[l] + nums[r]
			if sum == target {
				return sum
			}
			update(sum)
			if sum > target {
				for l < r && nums[r] == nums[r-1] {
					r--
				}
				r--
			} else {
				for l < r && nums[l] == nums[l+1] {
					l++
				}
				l++
			}
		}
	}
	return best
}

func abs(x int) int {
	if x < 0 {
		return -1 * x
	}
	return x
}

